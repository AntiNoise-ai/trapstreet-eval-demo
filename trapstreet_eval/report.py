"""Render eval results as markdown — both per-agent and head-to-head comparison."""

from __future__ import annotations

from datetime import datetime


def _fmt_usd(x: float) -> str:
    return f"${x:.4f}" if x < 1 else f"${x:.2f}"


def _fmt_ms(ms: int) -> str:
    if ms >= 1000:
        return f"{ms / 1000:.1f}s"
    return f"{ms}ms"


def render_run(result: dict) -> str:
    pct = result["accuracy"] * 100
    mode = result.get("mode", "evidence")
    lines = [
        f"# Trapstreet Eval — {result['agent']}",
        "",
        f"_FinanceBench OSS subset · {result['total']} questions · "
        f"mode=`{mode}` · {datetime.now():%Y-%m-%d %H:%M}_",
        "",
        f"**Accuracy**: {result['correct']}/{result['total']} ({pct:.1f}%)  ",
        f"**Latency**: p50 {_fmt_ms(result['latency_p50_ms'])} · p95 {_fmt_ms(result['latency_p95_ms'])}  ",
        f"**Cost**: {_fmt_usd(result['cost_usd'])}",
        "",
        "## Per-question results",
        "",
        "| # | Company | Question | Gold | Predicted | ✓ | Method |",
        "|---|---------|----------|------|-----------|---|--------|",
    ]
    for i, row in enumerate(result["rows"], 1):
        mark = "✅" if row["correct"] else "❌"
        q = (row["question"][:80] + "…") if len(row["question"]) > 80 else row["question"]
        gold = (row["gold"][:40] + "…") if len(row["gold"]) > 40 else row["gold"]
        pred = (row["predicted"][:40] + "…") if len(row["predicted"]) > 40 else row["predicted"]
        lines.append(
            f"| {i} | {row['company']} | {q} | {gold} | {pred} | {mark} | {row['grade_method']} |"
        )
    return "\n".join(lines) + "\n"


def render_compare(results: list[dict]) -> str:
    if not results:
        return "# Trapstreet Eval — no results\n"

    n = results[0]["total"]
    mode = results[0].get("mode", "evidence")
    mode_blurb = {
        "evidence": "agents see only the gold evidence passage",
        "pdf": "agents see the full 10-K filing text — closer to the real FinanceBench task",
    }.get(mode, mode)
    lines = [
        "# Trapstreet Eval — Head-to-Head",
        "",
        f"_FinanceBench OSS subset · {n} questions · "
        f"mode=`{mode}` ({mode_blurb}) · {datetime.now():%Y-%m-%d %H:%M}_",
        "",
        "## Leaderboard",
        "",
        "| Agent | Accuracy | Latency p50 | Latency p95 | Cost |",
        "|-------|----------|-------------|-------------|------|",
    ]
    sorted_results = sorted(results, key=lambda r: -r["accuracy"])
    for r in sorted_results:
        pct = r["accuracy"] * 100
        lines.append(
            f"| {r['agent']} | {r['correct']}/{r['total']} ({pct:.1f}%) "
            f"| {_fmt_ms(r['latency_p50_ms'])} "
            f"| {_fmt_ms(r['latency_p95_ms'])} "
            f"| {_fmt_usd(r['cost_usd'])} |"
        )

    lines += [
        "",
        "## Where they disagree",
        "",
        "Questions where at least one agent was wrong:",
        "",
    ]
    by_id: dict[str, dict] = {}
    for r in results:
        for row in r["rows"]:
            entry = by_id.setdefault(
                row["id"],
                {
                    "company": row["company"],
                    "question": row["question"],
                    "gold": row["gold"],
                    "preds": {},
                },
            )
            entry["preds"][r["agent"]] = (row["correct"], row["predicted"])

    disagreements = [
        e for e in by_id.values()
        if any(not c for c, _ in e["preds"].values())
    ]
    if not disagreements:
        lines.append("_All agents got every question right — try `--no-judge` or expand the subset._")
    else:
        header_agents = list(results[0]["rows"] and [r["agent"] for r in results])
        lines.append("| Question | Gold | " + " | ".join(header_agents) + " |")
        lines.append("|----------|------|" + "|".join(["---"] * len(header_agents)) + "|")
        for e in disagreements[:10]:  # cap to keep the report readable
            q = (e["question"][:70] + "…") if len(e["question"]) > 70 else e["question"]
            gold = (e["gold"][:30] + "…") if len(e["gold"]) > 30 else e["gold"]
            cells = []
            for a in header_agents:
                c, p = e["preds"].get(a, (False, "—"))
                p_short = (p[:25] + "…") if p and len(p) > 25 else (p or "—")
                cells.append(("✅ " if c else "❌ ") + p_short)
            lines.append(f"| {q} | {gold} | " + " | ".join(cells) + " |")
        if len(disagreements) > 10:
            lines.append("")
            lines.append(f"_…{len(disagreements) - 10} more in `results/compare.json`_")

    lines += [
        "",
        "---",
        "",
        "_Generated by [trapstreet-eval-demo](https://github.com/AntiNoise-ai/trapstreet-eval-demo) · "
        "cases curated via "
        "[AntiNoise-ai/market-research](https://github.com/AntiNoise-ai/market-research) · "
        "data: [PatronusAI/FinanceBench](https://huggingface.co/datasets/PatronusAI/financebench) "
        "(CC-BY-NC-4.0)_",
    ]
    return "\n".join(lines) + "\n"
